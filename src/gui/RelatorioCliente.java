    private void bntRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntRemoverActionPerformed
        try {   
            if (listCliente.getSelectedValue() != null) {
                model.Cliente cliente = new model.Cliente();
                cliente.setNome(txtNome.getText());
                cliente.setCnpj(txtCnpj.getText());
                cliente.setId(Integer.parseInt(txtId.getText()));
                // TODO add your handling code here:
                try {
                    ControleCliente.excluir(cliente.getId());
                
                } catch (SQLException ex) {
                    Logger.getLogger(model.Cliente.class.getName()).log(Level.SEVERE, null, ex);
                }}
                try {
                    listCliente.setListData(ControleCliente.listarTodos().toArray());
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }

            JOptionPane.showMessageDialog(null, "Cliente removido");
        }
        
        finally{
             JOptionPane.showMessageDialog(null, "Se o Cliente tiver serviço pendentes, não será possivel remover até que o serviço seja concluido ou descadastrado");
        }
    }//GEN-LAST:event_bntRemoverActionPerformed

    private void listClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listClienteMouseClicked
        try {
            model.Cliente c = (model.Cliente) listCliente.getSelectedValue();
            txtNome.setText(c.getNome());
            txtCnpj.setText(c.getCnpj());
            txtId.setText(c.getId().toString());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Não existe Clientes");
        }
        
    }//GEN-LAST:event_listClienteMouseClicked

