    private void bntAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntAlterarActionPerformed
        // TODO add your handling code here:
        
        model.Cliente cliente = new model.Cliente();
        cliente.setNome(txtNome.getText());
        cliente.setCnpj(txtCnpj.getText());
        cliente.setId(Integer.parseInt(txtId.getText()));
        try {
            ControleCliente.alterar(cliente);
        } catch (SQLException ex) {
            Logger.getLogger(model.Servico.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            listCliente.setListData(ControleCliente.listarTodos().toArray());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        //limpar tela;
        txtNome.setText("");
        txtCnpj.setText("");
        txtId.setText("");
        JOptionPane.showMessageDialog(null, "Cliente: " + cliente.getNome() + " Atualizado");
    }//GEN-LAST:event_bntAlterarActionPerformed

